    private void codeTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_codeTxtActionPerformed
        String SQL = "Select * From Item where code='" + codeTxt.getText() + "'";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/ThogaKade", "admin", "12345");
            Statement stm = connection.createStatement();
            ResultSet rst = stm.executeQuery(SQL);
            if (rst.next()) {
                deleteBtn.setEnabled(true);
                descriptionTxt.setText(rst.getString("description"));
                unitPriceTxt.setText(rst.getString("unitPrice"));
                qtyTxt.setText(rst.getString("qtyOnHand"));
            } else {
                JOptionPane.showMessageDialog(this, "No any Item found...");
                clearAll();
            }

        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_codeTxtActionPerformed

    public void clearAll() {
        codeTxt.setText(null);
        descriptionTxt.setText(null);
        unitPriceTxt.setText(null);
        qtyTxt.setText(null);
        deleteBtn.setEnabled(false);
    }
