    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Do you want to delete item number " + codeTxt.getText(), "Please select", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            String SQL = "Delete From Item where code='" + codeTxt.getText() + "'";
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/ThogaKade", "admin", "12345");
                Statement stm = connection.createStatement();
                int res = stm.executeUpdate(SQL);
                if (res > 0) {
                    JOptionPane.showMessageDialog(this, "Item Deleted Succusfully...");
                } else {
                    JOptionPane.showMessageDialog(this, "Something went wrong!");
                }
                clearAll();
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

